/**
 * Get UI translations for a given language.
 * Falls back to English if language not found.
 * Logs missing keys in debug mode.
 */
export function getUI(lang) {
  const l = (lang || 'en').toLowerCase();
  const dict = uiTranslations[l] || uiTranslations['en'];
  // Proxy to log missing keys in dev
  return new Proxy(dict, {
    get(target, key) {
      if (!(key in target) && key !== '__isProxy') {
        const fallback = uiTranslations['en'][key];
        if (fallback !== undefined) {
          console.warn(`[i18n] Missing translation key "${key}" for lang "${l}" — falling back to EN`);
          return fallback;
        }
        console.error(`[i18n] MISSING translation key "${key}" for lang "${l}" — no EN fallback!`);
        return `[${key}]`;
      }
      return target[key];
    }
  });
}